
public class StringApp {

	public static void main(String[] args) {
		
		System.out.println("Hello World"); // String은 Character들이 모여있는 것.
		System.out.println('H'); // Java에서는 '," 구분!! '=Character(문자, 한 글자를 표현하는 데이터타입)
		System.out.println("H"); // String 문자열
		
		System.out.println("Hello "
				+ "World");
		// new line
		System.out.println("Hello \nWorld");
		
		// escape \를 통해서 뒤에 따라오는 임무가 있는 문자의 임무를 일시적으로 도망,해방시키는 것.
		System.out.println("Hello \"World\""); // Hello "World"
	}

}
